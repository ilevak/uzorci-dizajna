public class Koordinata {
    private final double sirina;
    private final double duzina;

    public Koordinata(double sirina, double duzina) {
        this.sirina = sirina;
        this.duzina = duzina;
    }

    public double getSirina() {
        return sirina;
    }

    public double getDuzina() {
        return duzina;
    }
    
    
    
    
}
